package day_29_nestedloop_arrays;

public class MultiplicationTable {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            for( int a = 1; a <= 10; a++){
                System.out.print(i*a+ "\t");
            }
            System.out.println();

        }

    }

}
